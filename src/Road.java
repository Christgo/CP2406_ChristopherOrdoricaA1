public class Road {
    public int roadLength;
    public int roadNumber;


    public Road(int roadLength, int roadNumber) {
        this.roadLength = roadLength;
        this.roadNumber = roadNumber;
    }

    public int getRoadLength() {
        return roadLength;
    }

    public void setRoadLength(int roadLength) {
        this.roadLength = roadLength;
    }
}
